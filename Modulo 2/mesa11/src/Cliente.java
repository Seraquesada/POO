public class Cliente {
    private Integer numCliente;
    private String apellido;
    private String dni;
    private String CUIT;

    public Cliente(Integer numCliente, String apellido, String dni, String CUIT) {
        this.numCliente = numCliente;
        this.apellido = apellido;
        this.dni = dni;
        this.CUIT = CUIT;
    }


}
